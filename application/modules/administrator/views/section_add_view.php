<script type="text/javascript">
    function scrollToError(yeah) {
        $('html, body').animate({scrollTop:$('#'+yeah).offset().top - 50}, 'slow');
    }

    function submitform() {
        $("#form-edit").submit();
    }

    $('form').submit(function() {
        $(".error").remove();
        var idElementError = "";
        var hasError = false;
        var emailReg = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;

        var Title = $("#sectionTitle").val();
        if(Title == '') {
            $("#sectionTitle").before('<div class="error Required">Tiêu đề không được trống</div>');
            hasError = true;
            if (idElementError == "") idElementError = "sectionTitle";
        }

        var img_upload = $("#img_upload").attr("src");
        if(img_upload == '') {
            $("#img_upload").before('<div class="error Required">Ảnh section không được trống</div>');
            hasError = true;
            if (idElementError == "") idElementError = "img_upload";
        }

        var sectionOrders = $("#sectionOrders").val();
        if(isNaN(sectionOrders) || sectionOrders < 0) {
            $("#sectionOrders").before('<span class="error Required  ">Thứ tự phải là các chữ số và lớn hơn 0</span>');
            hasError = true;
            if (idElementError == "") idElementError = "sectionOrders";
        }


        if(hasError == false) {
            return true;
        }

        var index = $('#tabs a[href="#tabs-1"]').parent().index(); $('#tabs').tabs('select', index);

        scrollToError(idElementError);

        return false;
    });
</script>